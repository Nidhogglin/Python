def bmp_info(data):
    data_tuple = struct.unpack('<ccIIIIIIHH', data[:30])
    # print(data_tuple)
    if data_tuple[0] == b'B' and data_tuple[1] == b'M':
        return {
            'width': data_tuple[6],
            'height': data_tuple[7],
            'color': data_tuple[-1]
        }
    return 'Not bmp'


